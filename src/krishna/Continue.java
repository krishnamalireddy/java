package krishna;

public class Continue {

	public static void main(String[] args) {
		System.out.println("continue");
		int b=3;
		for(int i=0;i<10;i++){
			System.out.println(i);
			if(i == b) continue;
			System.out.println("-----");
			if(i == 5) break;}
			System.out.println("system error");
			
			
			
		
			
			
	}

}
